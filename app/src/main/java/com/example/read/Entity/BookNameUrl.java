package com.example.read.Entity;

public class BookNameUrl {
    String booknameurl;

    public BookNameUrl() {

    }

    public String getBooknameurl() {
        return booknameurl;
    }

    public void setBooknameurl(String booknameurl) {
        this.booknameurl = booknameurl;
    }

    @Override
    public String toString() {
        return "BookNameUrl{" +
                "booknameurl='" + booknameurl + '\'' +
                '}';
    }
}
